//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

import _ from 'lodash';

//-----------------------------------------------------------------------------//

const initialState = {
  inGame: false,
  mode:   null
}

//-----------------------------------------------------------------------------//
// Reducer
//-----------------------------------------------------------------------------//

function reducer (prevState=initialState, action){
  switch (action.type){

    case 'SET_MODE': {
      let state = _.clone(prevState);
      state.mode = action.mode;
      return state;
    }

    case 'UPDATE_INGAME': {
      let state = _.clone(prevState);
      state.inGame = action.flag;
      return state
    }

    default: {
      return prevState;
    }
  }
}

//-----------------------------------------------------------------------------//
// Export
//-----------------------------------------------------------------------------//

export default reducer

