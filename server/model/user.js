//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

const mongoose = require('mongoose');
const Schema   = mongoose.Schema;

//-----------------------------------------------------------------------------//
// Define a schema
//-----------------------------------------------------------------------------//

const schema = new Schema({
    username:  String,
    name:      String,
    coin:      String,
    rank:      String,
    date: { 
        type: Date, 
        default: Date.now 
    }
});

//-----------------------------------------------------------------------------//
// Compile our model
//-----------------------------------------------------------------------------//

const Model = mongoose.model('user', schema);

//-----------------------------------------------------------------------------//
// Export
//-----------------------------------------------------------------------------//

exports.Model = Model;

