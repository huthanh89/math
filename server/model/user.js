//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

const mongoose = require('mongoose');
const Schema   = mongoose.Schema;

//-----------------------------------------------------------------------------//
// Define a schema
//-----------------------------------------------------------------------------//

const schema = new Schema({
    username:   String,
    coin:       Number,
    rank:       String,
    difficulty: Number,
    rank:       Number,
    updated: { 
        type:    Date, 
        default: Date.now 
    },
    created: Date

});

//-----------------------------------------------------------------------------//
// Compile our model
//-----------------------------------------------------------------------------//

const Model = mongoose.model('user', schema);

//-----------------------------------------------------------------------------//
// Export
//-----------------------------------------------------------------------------//

module.exports = Model;

