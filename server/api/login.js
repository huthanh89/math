//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

const User = require('../model/user.js');

//-----------------------------------------------------------------------------//
// API Route
//-----------------------------------------------------------------------------//

const route = function(app){
    
    app.route('/api/login')
        
        .get(function (req, res) {
            User.findOne({
                email:    req.query.email,
                password: req.query.password
            },function (err, doc) {
                if (err) {
                    console.log(err);
                } 
                else {
                    res.send(doc)
                }
            });
        })

}

//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

module.exports = route;

