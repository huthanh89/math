const _    = require('lodash');
const User = require('../model/user.js');

//-----------------------------------------------------------------------------//
// API Route
//-----------------------------------------------------------------------------//

const route = function(app){
    app.route('/api/summary')
        .put(function (req, res) {
            User.findOne({
                _id: req.body.userID
            },function (err, user) {
                if (err) {
                    console.log(err);
                } 
                else {

                    user.coin += req.body.coin;

                    user.save(function (err, doc) {
                        if (err) {
                            console.log(err);
                        } 
                        else {
                            res.send(doc)
                        }
                    });

                }
            });
        })
}

//-----------------------------------------------------------------------------//
// Import
//-----------------------------------------------------------------------------//

module.exports = route;

